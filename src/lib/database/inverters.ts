export interface CommunicationProtocol {
  type: 'CAN' | 'RS485' | 'RS232' | 'USB' | 'WiFi' | 'Bluetooth';
  compatibleBms: string[];
}

export interface MPPTConfig {
  numberOfMppts: number;
  maxPvInputPerMppt: number; // kW
  mpptVoltageRange: { min: number; max: number }; // Volts
  maxInputCurrentPerMppt: number; // Amps
  maxStringsPerMppt: number;
}

export interface BatteryCompatibility {
  supportedVoltage: number[]; // e.g. [48, 51.2]
  batteryType: ('lithium' | 'leadacid' | 'gel' | 'agm')[];
  maxChargeCurrent: number; // Amps
  maxDischargeCurrent: number; // Amps
  communicationProtocols: CommunicationProtocol[];
  compatibleBatteryBrands: string[];
}

export interface InverterDatabaseEntry {
  id: string;
  brand: string;
  model: string;
  category: 'hybrid' | 'offgrid' | 'ongrid';
  ratedPower: number; // kW
  maxPvInput: number; // kW
  mppt: MPPTConfig;
  efficiency: number; // percentage
  gridVoltage: number;
  acOutputVoltage: number;
  acOutputFrequency: number;
  batteryCompatibility: BatteryCompatibility;
  dcAcRatio: number; // recommended DC/AC ratio
  maxDcInputVoltage: number; // Volts
  operatingTempRange: { min: number; max: number }; // Celsius
  dimensions: string;
  weight: number; // kg
  protectionRating: string; // IP65, IP66, etc
  warranty: number; // years
  availableInYemen: boolean;
  yemenDistributor?: string;
  enabled: boolean; // can be disabled
}

