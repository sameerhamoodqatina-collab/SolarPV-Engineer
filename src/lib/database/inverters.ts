export function getInvertersByCategory(category: 'hybrid' | 'offgrid' | 'ongrid'): InverterDatabaseEntry[] {
  return yemenInverters.filter((inv) => inv.category === category && inv.enabled);
}

export function getInvertersByBrand(brand: string): InverterDatabaseEntry[] {
  return yemenInverters.filter(
    (inv) => inv.brand.toLowerCase() === brand.toLowerCase() && inv.enabled
  );
}

export function getCompatibleInverters(
  batteryVoltage: number,
  batteryType: 'lithium' | 'leadacid' | 'gel' | 'agm',
  bmsBrand?: string
): InverterDatabaseEntry[] {
  return yemenInverters.filter((inv) => {
    if (!inv.enabled) return false;
    const bc = inv.batteryCompatibility;
    if (bc.supportedVoltage.length === 0) return false;
    if (!bc.supportedVoltage.includes(batteryVoltage)) return false;
    if (!bc.batteryType.includes(batteryType)) return false;
    if (bmsBrand) {
      const hasCompatibleBms = bc.communicationProtocols.some((proto) =>
        proto.compatibleBms.some(
          (bms) => bms.toLowerCase() === bmsBrand.toLowerCase()
        )
      );
      if (!hasCompatibleBms) return false;
    }
    return true;
  });
}

export function searchInverters(query: string): InverterDatabaseEntry[] {
  const lowerQuery = query.toLowerCase();
  return yemenInverters.filter((inv) => {
    if (!inv.enabled) return false;
    return (
      inv.brand.toLowerCase().includes(lowerQuery) ||
      inv.model.toLowerCase().includes(lowerQuery) ||
      inv.category.toLowerCase().includes(lowerQuery) ||
      inv.id.toLowerCase().includes(lowerQuery) ||
      inv.ratedPower.toString().includes(lowerQuery)
    );
  });
}

export function getInverterById(id: string): InverterDatabaseEntry | undefined {
  return yemenInverters.find((inv) => inv.id === id);
}
