export function getBatteriesByBrand(brand: string): BatteryDatabaseEntry[] {
  return yemenBatteries.filter(
    (b) => b.brand.toLowerCase() === brand.toLowerCase()
  );
}

export function getBatteriesByType(
  type: 'lithium' | 'leadacid' | 'gel' | 'agm'
): BatteryDatabaseEntry[] {
  return yemenBatteries.filter((b) => b.type === type);
}

export function getBatteriesByVoltage(voltage: number): BatteryDatabaseEntry[] {
  return yemenBatteries.filter(
    (b) => Math.abs(b.specs.nominalVoltage - voltage) < 1
  );
}

export function getCompatibleBatteries(inverterBrand: string): BatteryDatabaseEntry[] {
  return yemenBatteries.filter((b) =>
    b.communication.compatibleInverters.some(
      (i) => i.toLowerCase() === inverterBrand.toLowerCase()
    )
  );
}

export function searchBatteries(query: string): BatteryDatabaseEntry[] {
  const lower = query.toLowerCase();
  return yemenBatteries.filter(
    (b) =>
      b.brand.toLowerCase().includes(lower) ||
      b.model.toLowerCase().includes(lower) ||
      b.id.toLowerCase().includes(lower) ||
      (b.subType && b.subType.toLowerCase().includes(lower))
  );
}

export function getBatteryById(id: string): BatteryDatabaseEntry | undefined {
  return yemenBatteries.find((b) => b.id === id);
}
