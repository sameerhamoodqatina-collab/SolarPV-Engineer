export function getPanelsByManufacturer(manufacturer: string): SolarPanelData[] {
  return solarPanels.filter(p => p.manufacturer === manufacturer);
}

export function getPanelById(id: string): SolarPanelData | undefined {
  return solarPanels.find(p => p.id === id);
}

export function searchPanels(query: string): SolarPanelData[] {
  const q = query.toLowerCase();
  return solarPanels.filter(
    p =>
      p.model.toLowerCase().includes(q) ||
      p.manufacturer.toLowerCase().includes(q)
  );
}

export function getPanelsByPowerRange(min: number, max: number): SolarPanelData[] {
  return solarPanels.filter(p => p.power >= min && p.power <= max);
}

export function getHighestEfficiencyPanels(count: number): SolarPanelData[] {
  return [...solarPanels]
    .sort((a, b) => b.efficiency - a.efficiency)
    .slice(0, count);
}
