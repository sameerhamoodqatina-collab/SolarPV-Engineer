export function getBatteriesByManufacturer(manufacturer: string): BatteryData[] {
  return batteries.filter(b => b.manufacturer === manufacturer);
}

export function getBatteryById(id: string): BatteryData | undefined {
  return batteries.find(b => b.id === id);
}

export function getBatteriesByType(type: BatteryData['type']): BatteryData[] {
  return batteries.filter(b => b.type === type);
}

export function searchBatteries(query: string): BatteryData[] {
  const q = query.toLowerCase();
  return batteries.filter(
    b =>
      b.model.toLowerCase().includes(q) ||
      b.manufacturer.toLowerCase().includes(q)
  );
}

export function getBatteriesByKwhRange(min: number, max: number): BatteryData[] {
  return batteries.filter(b => b.kwh >= min && b.kwh <= max);
}
